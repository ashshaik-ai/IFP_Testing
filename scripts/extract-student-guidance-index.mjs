import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const source = fs.readFileSync(path.join(ROOT, 'student-guidance.html'), 'utf8');
const sectionTitle = new Map();

for (const match of source.matchAll(/<section[^>]*id="([^"]+)"[\s\S]*?<h2[^>]*>([\s\S]*?)<\/h2>/g)) {
  sectionTitle.set(match[1], strip(match[2]));
}

const cards = [];
let currentSection = 'student-guidance';
const blocks = source.split(/(?=<\/?section\b|<div class="gx-card")/g);

for (const block of blocks) {
  const sectionMatch = block.match(/^<section[^>]*id="([^"]+)"/);
  if (sectionMatch) currentSection = sectionMatch[1];
  if (!block.startsWith('<div class="gx-card"')) continue;

  const stream = attr(block, 'data-stream');
  const search = attr(block, 'data-search');
  const title = strip((block.match(/<span class="gx-title">([\s\S]*?)(?:<span class="gx-tag">|<\/span>)/) || [])[1] || '');
  const tag = strip((block.match(/<span class="gx-tag">([\s\S]*?)<\/span>/) || [])[1] || '');
  const summary = strip((block.match(/<span class="gx-best">([\s\S]*?)<\/span>/) || [])[1] || '');

  cards.push({
    id: slug(`${currentSection}-${title || cards.length + 1}`),
    section: currentSection,
    sectionTitle: sectionTitle.get(currentSection) || currentSection,
    title,
    tag,
    summary,
    streams: stream.split(/\s+/).filter(Boolean),
    search,
    url: `student-guidance.html#${currentSection}`
  });
}

const output = [
  '/* Auto-generated by scripts/extract-student-guidance-index.mjs.',
  '   Compact authoring/search index only; student-guidance.html remains the rendering source for now. */',
  '(function () {',
  '  window.IF_STUDENT_GUIDANCE_INDEX = ' + JSON.stringify({ generatedAt: new Date().toISOString(), count: cards.length, cards }, null, 2) + ';',
  "  if (typeof module !== 'undefined' && module.exports) module.exports = window.IF_STUDENT_GUIDANCE_INDEX;",
  '})();',
  ''
].join('\n');

fs.writeFileSync(path.join(ROOT, 'assets/data/student-guidance-index.js'), output, 'utf8');
console.log(`Extracted ${cards.length} Student Guidance cards.`);

function attr(html, name) {
  const match = html.match(new RegExp(`${name}="([^"]*)"`));
  return match ? decode(match[1]) : '';
}

function strip(html) {
  return decode(String(html || '')
    .replace(/<[^>]+>/g, ' ')
    .replace(/\s+/g, ' ')
    .trim());
}

function decode(value) {
  return String(value)
    .replace(/&quot;/g, '"')
    .replace(/&amp;/g, '&')
    .replace(/&#39;/g, "'")
    .replace(/&nbsp;/g, ' ');
}

function slug(value) {
  return String(value || '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .slice(0, 80);
}
